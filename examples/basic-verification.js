const { hashDocument } = require('../src/hash');
const { generateKeyPair, createSignedAttestation, buildAttestation, verifySignature } = require('../src/sign');

// --- Step 1: Simulate an AI-generated legal document ---
const document = `
EMPLOYMENT AGREEMENT

This Employment Agreement ("Agreement") is entered into as of March 1, 2026,
by and between Acme Corp ("Employer") and Jane Doe ("Employee").

1. POSITION: Employee shall serve as Senior Software Engineer.
2. COMPENSATION: Base salary of $150,000 per annum.
3. TERM: This agreement is at-will and may be terminated by either party.

[Generated by AI Legal Assistant]
`;

console.log('=== LegalSeal Protocol — Basic Verification Example ===\n');

// --- Step 2: Hash the document ---
const documentHash = hashDocument(document);
console.log('1. Document hashed (SHA-256):', documentHash);

// --- Step 3: Generate attorney key pair ---
const { publicKey, privateKey } = generateKeyPair();
console.log('2. Attorney RSA key pair generated');

// --- Step 4: Create signed attestation ---
const attestation = createSignedAttestation({
  documentHash,
  attorneyName: 'Sarah Chen',
  barNumber: 'CA-298371',
  jurisdiction: 'California',
  reviewDate: new Date().toISOString(),
}, privateKey);

console.log('3. Attestation signed');
console.log('   Attorney:', attestation.attorneyName);
console.log('   Bar #:', attestation.barNumber);
console.log('   Jurisdiction:', attestation.jurisdiction);
console.log('   Signature:', attestation.signature.substring(0, 40) + '...');

// --- Step 5: Verify the signature ---
const payload = buildAttestation({
  documentHash: attestation.documentHash,
  attorneyName: attestation.attorneyName,
  barNumber: attestation.barNumber,
  jurisdiction: attestation.jurisdiction,
  reviewDate: attestation.reviewDate,
  statement: attestation.attestationStatement,
});

const isValid = verifySignature(payload, attestation.signature, publicKey);
console.log('\n4. Signature verification:', isValid ? 'VALID' : 'INVALID');

// --- Step 6: Demonstrate tamper detection ---
const tamperedPayload = buildAttestation({
  documentHash: attestation.documentHash,
  attorneyName: 'Fake Attorney',
  barNumber: attestation.barNumber,
  jurisdiction: attestation.jurisdiction,
  reviewDate: attestation.reviewDate,
  statement: attestation.attestationStatement,
});

const isTamperedValid = verifySignature(tamperedPayload, attestation.signature, publicKey);
console.log('5. Tampered payload verification:', isTamperedValid ? 'VALID (BAD!)' : 'INVALID (tamper detected)');

console.log('\n=== Done ===');
